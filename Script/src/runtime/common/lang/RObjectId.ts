//==========================================================
export class RObjectId {
   //..........................................................
   // @attribute
   private static _hash: number = 1;

   //==========================================================
   // <T>获得下一个编号。</T>
   //
   // @method
   // @return Integer 编号
   //==========================================================
   public static nextId() {
      return this._hash++;
   }
}
