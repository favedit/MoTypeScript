import {Objects} from '../../common/lang/Objects';

//==========================================================
// <T>插件接口。</T>
//
// @reference
// @author maocy
// @version 160309
//==========================================================
export interface PluginFace {
   // 类型
   type: string;
   // 名称
   name: string;
   // 有效
   enable: boolean;
   // 描述
   description: string;
   // 依赖
   dependencies: Objects<PluginFace>;

   //==========================================================
   // <T>创建处理。</T>
   //==========================================================
   onCreate(context);

   //==========================================================
   // <T>激活处理。</T>
   //==========================================================
   onActive(element, value);

   //==========================================================
   // <T>取消激活处理。</T>
   //==========================================================
   onDeactive(dataAndEvents);

   //==========================================================
   // <T>销毁处理。</T>
   //==========================================================
   onDestroy(up);
}