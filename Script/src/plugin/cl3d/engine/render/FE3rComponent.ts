import {FE3rObject} from './FE3rObject';

//==========================================================
// <T>渲染3D对象。</T>
//
// @class
// @author maocy
// @history 150206
//==========================================================
export class FE3rComponent extends FE3rObject {
   //..........................................................
   // @attribute
   public statusLoading = false;

   //==========================================================
   // <T>构造处理。</T>
   //
   // @method
   //==========================================================
   public constructor() {
      super();
   }

   //==========================================================
   // <T>处理加载开始。</T>
   //
   // @method
   //==========================================================
   public processLoadBegin() {
      this.statusLoading = true;
   }

   //==========================================================
   // <T>处理加载结束。</T>
   //
   // @method
   //==========================================================
   public processLoadEnd() {
      this.statusLoading = false;
   }

   //==========================================================
   // <T>释放处理。</T>
   //
   // @method
   //==========================================================
   public dispose() {
      super.dispose();
   }

}