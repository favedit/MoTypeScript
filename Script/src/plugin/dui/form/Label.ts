import {Control} from './Control';

//==========================================================
// <T>文本控件。</T>
//
// @author maocy
// @version 160408
//==========================================================
export class Label extends Control {
   //==========================================================
   // <T>构造处理。</T>
   //==========================================================
   public constructor() {
      super();
      // 设置属性
      this.renderOptionFragment = false;
   }

   //==========================================================
   // <T>获取内容。</T>
   //
   // @return 内容
   //==========================================================
   public get() {
      return this._hPanel.innerHTML;
   }

   //==========================================================
   // <T>设置内容。</T>
   //
   // @param value 内容
   //==========================================================
   public set(value) {
      this._hPanel.innerHTML = value;
   }
}