import {ObjectBase} from '../../../runtime/common/lang/ObjectBase';

//==========================================================
// <T>请求处理。</T>
//==========================================================
export class FRequest extends ObjectBase {
   // 是否提交
   public isCommitted = false;

   //==========================================================
   // <T>构造处理。</T>
   //==========================================================
   public constructor() {
      super();
   }

   //==========================================================
   // <T>撤销处理。</T>
   //==========================================================
   public onUndo(): void {
   }

   //==========================================================
   // <T>撤销处理。</T>
   //==========================================================
   public undo(): void {
      this.onUndo();
   }

   //==========================================================
   // <T>重做处理。</T>
   //==========================================================
   public onRedo(): void {
   }

   //==========================================================
   // <T>重做处理。</T>
   //==========================================================
   public redo(): void {
      this.onRedo();
   }

   //==========================================================
   // <T>提交处理。</T>
   //==========================================================
   public onCommit(): void {
   }

   //==========================================================
   // <T>提交处理。</T>
   //==========================================================
   public commit(): void {
      this.onCommit();
      this.isCommitted = true;
   }
}
